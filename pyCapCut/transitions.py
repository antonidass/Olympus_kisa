def is_long(name: str) -> bool:
    """True → взять увеличенную длительность перехода."""
    return name in _LONG_KEYS


def resolve_name(name: str) -> str:
    """Русское имя → внутреннее имя enum (китайское/английское).
    Если имя уже «внутреннее» (не в словаре) — возвращаем его как есть,
    пусть pyCapCut попробует найти его в enum напрямую."""
    key = name.strip().lower()
    return RU_TO_CN.get(key, name)


def resolve(name: str, transition_enum) -> Optional[object]:
    """Возвращает значение TransitionType.<...> для русского или
    китайского имени. None — если ни так ни так не нашлось."""
    internal = resolve_name(name)
    if hasattr(transition_enum, internal):
        return getattr(transition_enum, internal)
    # Второй шанс — вдруг пользователь написал китайское имя в оригинале,
    # но мы его пропустили через lower() и испортили. Пробуем исходную
    # строку без модификаций.
    if hasattr(transition_enum, name):
        return getattr(transition_enum, name)
    return None


